# ===========================================================================
# 激活配置（根据 ACTIVE_CONFIG 选择）
# ===========================================================================
if ACTIVE_CONFIG == "42":
    STATIONS     = _STATIONS_42
    ROBOT_STARTS = _ROBOT_STARTS_42
    POD_TASKS    = _POD_TASKS_42
else:
    STATIONS     = _STATIONS_10
    ROBOT_STARTS = _ROBOT_STARTS_10
    POD_TASKS    = _POD_TASKS_10

# 固定障碍（无，开放地图）
OBSTACLES: set[Pos] = set()


# ===========================================================================
# 构建 Agent 和 Task 列表
# ===========================================================================
def build_agents_and_tasks() -> tuple[list[Agent], list[Task]]:
    tasks: list[Task] = []
    for tid, (pod_pos, station_id) in enumerate(POD_TASKS):
        st_pos = STATIONS[station_id]
        tasks.append(Task(
            task_id=tid,
            pod_pos=pod_pos,
            station_pos=st_pos,
            station_id=station_id,
        ))

    agents: list[Agent] = []
    for aid, start in enumerate(ROBOT_STARTS):
        agents.append(Agent(agent_id=aid, start=start))

    return agents, tasks
